package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "flow",
	Short: "A CLI tool to streamline the use of Git Flow and Conventional Commits.",
	Long: `
  Flower is a CLI tool developers that follow the patterns of Git Flow and Conventional Commits.
  
  For example, you could automatically prefix each commit with a Conventional Commit and make a Git Flow
  branch structure`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

